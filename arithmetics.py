
class Arithmetics:
    def __init__(self):
        pass
    def addition(self, a, b):
        self.a = a
        self.b = b
        return (self.a + self.b)
    def subtraction(self, a, b):
        self.a = a
        self.b = b
        return (self.a - self.b)
    def multiplication(self, a, b):
        self.a = a
        self.b = b
        return (self.a * self.b)
    def division(self, a, b):
        self.a = a
        self.b = b
        return (self.a / self.b) 
    def square(self, a):   
        self.a = a 
        return(self.a * self.a) 
       
 
